package Test_5_calculator_parameter;

/**
 * 带参方法
 */
public class Calcalutor {

    //求两个数的和
    public double plus(double num1,double num2){
        double result = num1 + num2;
        return result;
    }

    public double minus(double num1,double num2){
        double result = num1 - num2;
        return result;
    }

}
